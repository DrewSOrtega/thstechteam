		<ul>
			<li>CP2 is based on the text C++ in 24 Hours. Using Visual Basic, the students learn basic coding techniques, problem solving, testing and debugging. Concepts covered will include basic data types, input, output, looping, branching, file access, arrays, data structures, objects, functions and libraries. Lab activities will involve problem solving, robotic control, and business applications. Learning activities will involve some technical reading, classroom discussions, short programming practice problems, applying programs to classroom robots and longer lab assignments.</li>
		</ul>
		<li><h3>AP Computer Science</h3></li>
		<ul>
			<li>This course has been developed by Florida State University in conjunction with the College Board to prepare the student to successfully challenge the AP Computer Science Examination. Using the BlueJ Integrated Development Environment, JAVA programming language and GradPoint online curriculum in conjunction with classroom instruction, students will learn to manipulate data using class methods, condition statements, logical operators, loops, single and multidimensional arrays, constructors, array lists, recursion, hierarchies, polymorphism, a variety of sorts, and a variety of searches, all culminating in a series of practice exams. At the completion of this course, the student will have the background and the confidence needed to successfully complete the AP Computer Science examination in May of each school year.</li>
